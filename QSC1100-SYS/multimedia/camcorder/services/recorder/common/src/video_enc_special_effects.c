/*===========================================================================

FUNCTION vse_identity

DESCRIPTION
  A Do nothing special effect using memcpy.
  
DEPENDENCIES
  None
 
ARGUMENTS IN    
  None
   
ARGUMENTS IN/OUT   
  
ARGUMENTS OUT
  None
      
RETURN VALUE
  None
        
SIDE EFFECTS
  None

===========================================================================*/

vse_return_type vse_identity( 
  vse_packet_data_type   *packet,
  vse_type               *vse_struct,
  vse_command_type        command
)
{
  uint32 nbytes;
  vse_return_type retval;
  vse_return_type retval_sub;

  nbytes = vse_struct->npix_x * vse_struct->npix_y *3/2;

  retval = VSE_SUCCESS;

  switch(command)
  {
  case VSE_RUN:
    /* execute the special effect
    */

    if ( vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED ) {
      retval_sub = vse_identity(packet,vse_struct,VSE_INIT_ENG);
    }

    if ( packet->status == VSE_PACKET_STATUS_UNINITIALIZED ) {

      retval_sub = vse_identity(packet,vse_struct,VSE_INIT_PACKET);
      if (retval_sub != VSE_SUCCESS) {
        retval = retval_sub;
        break;
      }
    }
    memcpy(packet->output_buffer->imgPtr,
           packet->input_buffer->imgPtr,nbytes);
    packet->free_buffer_flag = RELEASE_INPUT_BUFFER;
    break;

  case VSE_INIT_ENG:

    /* initialize the global variables relative to this special
    ** effect that are contained in vse_struct
    */

    if (vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED ) {
      vse_struct->engine_status = VSE_ENGINE_INITIALIZED;
#ifdef FEATURE_CAMCORDER_MALLOC
      {
        static msg_const_type m;
        vse_struct->malloc_context = 
          camcorderutil_malloc_init(__MODULE__,__LINE__,&m);
      }
#endif
    }
    break;

  case VSE_INIT_PACKET:

    /* initialize the packets
    */

    if ( packet->status != VSE_PACKET_STATUS_INITIALIZED ) {
    
      packet->output_buffer = 
        (ipl_image_type *) vse_malloc(sizeof(ipl_image_type));

      if (packet->output_buffer == NULL) {
        retval = VSE_MALLOC_FAILED;
        break;
      }

      packet->output_buffer->imgPtr = vse_malloc(nbytes);
      packet->output_buffer->dx = vse_struct->npix_x;
      packet->output_buffer->dy = vse_struct->npix_y;
      packet->output_buffer->cFormat = IPL_YCrCb420_LINE_PK;

      packet->status = VSE_PACKET_STATUS_INITIALIZED; 
    }
    else
    {
      retval = VSE_ALREADY_INITIALIZED;
    }
    break;

  case VSE_TERMINATE_PACKET:


    /* un-initialize the packets
    */

    if ( packet->status == VSE_PACKET_STATUS_INITIALIZED) {
      
      vse_free(packet->output_buffer->imgPtr);
      vse_free(packet->output_buffer);
      packet->output_buffer = NULL;
      packet->status = VSE_PACKET_STATUS_UNINITIALIZED;
    }
    else
    {
      retval = VSE_ALREADY_TERMINATED;
    }
    break;

  case VSE_TERMINATE:

    /* terminate the special effect
    */
    if (vse_struct->engine_status == VSE_ENGINE_INITIALIZED) {
      vse_struct->engine_status = VSE_ENGINE_UNINITIALIZED;
#ifdef FEATURE_CAMCORDER_MALLOC
      { static msg_const_type m;
        camcorderutil_malloc_terminate(vse_struct->malloc_context,
                                       __LINE__,__func__,&m);
        vse_struct->malloc_context = NULL;
      }
#endif
    }
    break;
  }
  

  return retval;
}

