/*===========================================================================

FUNCTION vse_fadeInOut

DESCRIPTION
  First call with the VSE_INIT_ENG command with a packet. The param of the
  input packet structure contains the nr. of msecs to fade in/out, and
  the function ID (either fade in or fade out).

  After the last frame of fade-in subsequent calls to vse_fadeInOut will
  return the identity function.

  After the last frame of fade-out subsequent calls to vse_fadeInOut will
  return the white image.  
  
DEPENDENCIES
  None
 
ARGUMENTS IN    
  None
   
ARGUMENTS IN/OUT   
  
ARGUMENTS OUT
  None
      
RETURN VALUE
  None
        
SIDE EFFECTS
  None

===========================================================================*/
vse_return_type vse_fadeInOut( 
  vse_packet_data_type   *packet,
  vse_type               *vse_struct,
  vse_command_type        command
)
{
  vse_return_type retval;
  vse_return_type retval_sub;

  retval = VSE_SUCCESS;

  switch(command)
  {
  case VSE_RUN:

    MP4E_DEBUG_RECORD_TIMELOG(MP4E_TIMELOG_ARM_SPECIAL_EFFECT_START,NULL,0);
    MSG_VSETIMING("start fadein/out %d Y=%x",mp4e_get_time_ms(),packet->input_buffer->imgPtr,0);

    if ( vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED ) {
      retval_sub = vse_overlay(packet,vse_struct,VSE_INIT_ENG);
      if (retval_sub != VSE_SUCCESS ) {
        retval = retval_sub;
        break;
      }
    }

    if ( packet->status == VSE_PACKET_STATUS_UNINITIALIZED ) {

      retval_sub = vse_overlay(packet,vse_struct,VSE_INIT_PACKET);
      if (retval_sub != VSE_SUCCESS) {
        retval = retval_sub;
        break;
      }
    }

    {
      vse_fadeInOut_internal_type *param;
      uint32 time;
      ipl_status_type iplres;
      uint32 alpha;
      boolean do_alpha_blend_flag;

      param = (vse_fadeInOut_internal_type *) vse_struct->param;

      time = packet->msec - param->starting_msec;
      do_alpha_blend_flag = TRUE;

      if (time >= param->duration_msec) {
        if (param->cmd_type == VSE_FADE_IN_COMMAND )
          do_alpha_blend_flag = FALSE;
        else
          alpha = 0;

        retval = VSE_COMPLETED;
      }
      else
      {
        alpha = 100* time /param->duration_msec;
        if ( param->cmd_type == VSE_FADE_OUT_COMMAND) {
          alpha = 100 - alpha;
        }
      }

      if (do_alpha_blend_flag) {
        /* in place alpha blending to white */
        iplres = ipl_compose(packet->input_buffer,NULL,packet->input_buffer,
                             IPL_BLEND_WHITE,&alpha);
        if (iplres != IPL_SUCCESS) {
          retval = VSE_FAILURE;
        }
      }
    }

    packet->free_buffer_flag = INPUT_BUFFER_USED;

    MP4E_DEBUG_RECORD_TIMELOG(MP4E_TIMELOG_ARM_SPECIAL_EFFECT_DONE,NULL,0);
    MSG_VSETIMING("done vse_fadeInOutParamsType %d",mp4e_get_time_ms(),0,0);
    if (retval != VSE_SUCCESS) {
      MSG_VSETIMING("vse failed",0,0,0);
    }

    break;

  case VSE_INIT_ENG:

    if (vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED) 
    {
      vse_fadeInOut_internal_type *param;
      vse_fadeInOutParamsType  *input_params;

#ifdef FEATURE_CAMCORDER_MALLOC
      {
        static msg_const_type m1;
        vse_struct->malloc_context = 
          camcorderutil_malloc_init(__func__,__LINE__,&m1);
      }
#endif
      param = (vse_fadeInOut_internal_type *) 
           vse_malloc(sizeof(vse_fadeInOut_internal_type));
      if(param == NULL)
      {
        retval = VSE_MALLOC_FAILED;
        break;
      }
      vse_struct->param = param;

      input_params = (vse_fadeInOutParamsType *) packet->param;

      param->duration_msec = input_params->nmsecs;
      param->starting_msec = packet->msec;
      param->cmd_type      = input_params->fadeInOutCommand;
     
      vse_struct->engine_status = VSE_ENGINE_INITIALIZED;
    }
    break;

  case VSE_INIT_PACKET:
    if ( packet->status != VSE_PACKET_STATUS_INITIALIZED ) {
      packet->status = VSE_PACKET_STATUS_INITIALIZED; 
    }
    else
    {
      retval = VSE_ALREADY_INITIALIZED;
    }
    break;

  case VSE_TERMINATE_PACKET:
    if ( packet->status == VSE_PACKET_STATUS_INITIALIZED) {
      packet->status = VSE_PACKET_STATUS_UNINITIALIZED;
    }
    else
    {
      retval = VSE_ALREADY_TERMINATED;
    }
    break;

  case VSE_TERMINATE:

    if (vse_struct->engine_status == VSE_ENGINE_INITIALIZED ) {
      vse_free(vse_struct->param);
      vse_struct->param = NULL;
#ifdef FEATURE_CAMCORDER_MALLOC
      {
        static msg_const_type m;
        camcorderutil_malloc_terminate(vse_struct->malloc_context,__LINE__,
                                       __func__,&m);
        vse_struct->malloc_context = NULL;
      }
#endif

      vse_struct->engine_status = VSE_ENGINE_UNINITIALIZED;
    }
    break;
  }
  
  return retval;
}

