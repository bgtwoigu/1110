typedef struct 
{
  uint16  msec_for_each_symbol;   /* lifetime of an image in msec */
  uint16  current_symbol;         /* indx to images_table[] */
  uint32  time_to_next_change;    /* time when next change will happen */
  ipl_rect_type   rect;           /* rect offset */
} vse_timestamp_type;


