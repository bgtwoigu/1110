/*===========================================================================

FUNCTION vse_timestamp_type

DESCRIPTION
  TimeStamp demonstration function.
  
DEPENDENCIES
  None
 
ARGUMENTS IN    
  None
   
ARGUMENTS IN/OUT   
  
ARGUMENTS OUT
  None
      
RETURN VALUE
  None
        
SIDE EFFECTS
  None

===========================================================================*/
vse_return_type vse_timestamp( 
  vse_packet_data_type   *packet,
  vse_type               *vse_struct,
  vse_command_type        command
)
{
  vse_return_type retval;
  vse_return_type retval_sub;
  vse_timestamp_type *timestamp_struct;
  ipl_image_type *frame;
  ipl_status_type ipl_retval;
  
  retval = VSE_SUCCESS;
  
  vse_timestamp_image.dx = 24;
  vse_timestamp_image.dy = 24;
  vse_timestamp_image.cFormat = IPL_RGB565;
  vse_timestamp_image.clrPtr = NULL;
  
  switch(command)
  {
  case VSE_RUN:

    MP4E_DEBUG_RECORD_TIMELOG(MP4E_TIMELOG_ARM_SPECIAL_EFFECT_START,NULL,0);
    MSG_VSETIMING("start timestamp  %d Y=%x",mp4e_get_time_ms(),packet->input_buffer->imgPtr,0);

    if ( vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED ) {
      retval_sub = vse_timestamp(packet,vse_struct,VSE_INIT_ENG);
      if (retval_sub != VSE_SUCCESS ) {
        retval = retval_sub;
        break;
      }
    }

    if ( packet->status == VSE_PACKET_STATUS_UNINITIALIZED ) {

      retval_sub = vse_timestamp(packet,vse_struct,VSE_INIT_PACKET);
      if (retval_sub != VSE_SUCCESS) {
        retval = retval_sub;
        break;
      }
      vse_timestamp_image.imgPtr = (unsigned char* )vse_malloc(SIZE_OF_IMAGE_DIGIT);
      if(vse_timestamp_image.imgPtr == NULL)
      {
        retval = VSE_FAILURE;
        break;
      }
    }
    
    if(vse_timestamp_image.imgPtr == NULL)
    {
      retval = VSE_FAILURE;
      break;
    }
    timestamp_struct = (vse_timestamp_type *) vse_struct->param;

    while(packet->msec >= timestamp_struct->time_to_next_change)
    {
      timestamp_struct->time_to_next_change +=
        timestamp_struct->msec_for_each_symbol;
      timestamp_struct->current_symbol++;
      if (timestamp_struct->current_symbol >= VSE_TIMESTAMP_NIMAGES )
        timestamp_struct->current_symbol = 0;
      memcpy(vse_timestamp_image.imgPtr, vse_digifont_24x24_digits[timestamp_struct->current_symbol], SIZE_OF_IMAGE_DIGIT);
      frame = &vse_timestamp_image;
      timestamp_struct->rect.dx  = frame->dx;
      timestamp_struct->rect.dy  = frame->dy;
      timestamp_struct->rect.x   = (packet->input_buffer->dx - frame->dx) - 5;
      timestamp_struct->rect.y   = (packet->input_buffer->dy - frame->dy) - 5;
    }

    memcpy(vse_timestamp_image.imgPtr, vse_digifont_24x24_digits[timestamp_struct->current_symbol], SIZE_OF_IMAGE_DIGIT);
    frame = &vse_timestamp_image;

    ipl_retval = ipl_image_add_inplace(packet->input_buffer,frame,
                                       &timestamp_struct->rect,63519);

    if (ipl_retval != IPL_SUCCESS)
    {
      retval = VSE_FAILURE;
    }
    packet->free_buffer_flag = INPUT_BUFFER_USED;

    MP4E_DEBUG_RECORD_TIMELOG(MP4E_TIMELOG_ARM_SPECIAL_EFFECT_DONE,NULL,0);
    MSG_VSETIMING("done overlay %d",mp4e_get_time_ms(),0,0);

    break;

  case VSE_INIT_ENG:

    if (vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED) 
    {
#ifdef FEATURE_CAMCORDER_MALLOC
      {
        static msg_const_type m;
        vse_struct->malloc_context =
          camcorderutil_malloc_init(__func__,__LINE__,&m);
      }
#endif

      if(vse_struct->param != NULL )
        CAMCORDER_MSG_ERROR("timestamp error",0,0,0);
      vse_struct->param = vse_malloc(sizeof(vse_timestamp_type));
      if(vse_struct->param == NULL)
      {
        retval = VSE_MALLOC_FAILED;
        break;
      }
      timestamp_struct = (vse_timestamp_type *) vse_struct->param;

      timestamp_struct->msec_for_each_symbol = 1000;
      timestamp_struct->time_to_next_change = packet->msec;
      timestamp_struct->current_symbol = VSE_TIMESTAMP_NIMAGES-1;

      vse_struct->npix_x = packet->input_buffer->dx;
      vse_struct->npix_y = packet->input_buffer->dy;

      vse_struct->engine_status = VSE_ENGINE_INITIALIZED;
    }
    break;

  case VSE_INIT_PACKET:
    if ( packet->status != VSE_PACKET_STATUS_INITIALIZED ) {
      packet->status = VSE_PACKET_STATUS_INITIALIZED; 
    }
    else
    {
      retval = VSE_ALREADY_INITIALIZED;
    }
    break;

  case VSE_TERMINATE_PACKET:
    if ( packet->status == VSE_PACKET_STATUS_INITIALIZED) {
      packet->status = VSE_PACKET_STATUS_UNINITIALIZED;
    }
    else
    {
      retval = VSE_ALREADY_TERMINATED;
    }
    vse_free(vse_timestamp_image.imgPtr);    
    break;

  case VSE_TERMINATE:

    if (vse_struct->engine_status == VSE_ENGINE_INITIALIZED ) {
      vse_free(vse_struct->param);
      vse_struct->param = NULL;

#ifdef FEATURE_CAMCORDER_MALLOC
      {
        static msg_const_type m;
        camcorderutil_malloc_terminate(vse_struct->malloc_context,__LINE__,
                                       __func__,&m);
        vse_struct->malloc_context = NULL;
      }
#endif

      vse_struct->engine_status = VSE_ENGINE_UNINITIALIZED;

    }
    vse_free(vse_timestamp_image.imgPtr);    
    break;
  }
  

  return retval;
}
