/*===========================================================================

FUNCTION vse_overlay

DESCRIPTION
  Overlay demonstration function.
  
DEPENDENCIES
  None
 
ARGUMENTS IN    
  None
   
ARGUMENTS IN/OUT   
  
ARGUMENTS OUT
  None
      
RETURN VALUE
  None
        
SIDE EFFECTS
  None

===========================================================================*/
vse_return_type vse_overlay( 
  vse_packet_data_type   *packet,
  vse_type               *vse_struct,
  vse_command_type        command
)
{
  vse_return_type retval;
  vse_return_type retval_sub;

  retval = VSE_SUCCESS;

  switch(command)
  {
  case VSE_RUN:

    MP4E_DEBUG_RECORD_TIMELOG(MP4E_TIMELOG_ARM_SPECIAL_EFFECT_START,NULL,0);
    MSG_VSETIMING("start overlay %d Y=%x",mp4e_get_time_ms(),packet->input_buffer->imgPtr,0);

    if ( vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED ) {
      retval_sub = vse_overlay(packet,vse_struct,VSE_INIT_ENG);
      if (retval_sub != VSE_SUCCESS ) {
        retval = retval_sub;
        break;
      }
    }

    if ( packet->status == VSE_PACKET_STATUS_UNINITIALIZED ) {

      retval_sub = vse_overlay(packet,vse_struct,VSE_INIT_PACKET);
      if (retval_sub != VSE_SUCCESS) {
        retval = retval_sub;
        break;
      }
    }
    {
      ipl_status_type ipl_retval;
      ipl_image_type *input;
      vse_overlay_param_type *param;

      param = (vse_overlay_param_type *) packet->param;

      if(VSE_OVERLAY_VALID_MAGIC != param->valid_check )
      {
        /* if it fails this check then something is wrong
        */
        CAMCORDER_MSG_ERROR("OVERLAY failed validity check",0,0,0);
        retval = VSE_FAILURE;
        break;
      }

      input = packet->input_buffer;
      /* This conversion need to be done only for the new frame, and once done
            * the same converted values can be used further.
            */
      if(param->new_frame)
      {
        param->ycbcrframe.cFormat = IPL_YCrCb420_LINE_PK;
        param->ycbcrframe.dx = param->frame->dx;
        param->ycbcrframe.dy = param->frame->dy;
        if(param->ycbcrframe.imgPtr)
        {
          vse_free(param->ycbcrframe.imgPtr);
        }
        if(param->ycbcrframe.clrPtr)
        {
          vse_free(param->ycbcrframe.clrPtr);
        }
        param->ycbcrframe.imgPtr = vse_malloc((param->ycbcrframe.dx)*(param->ycbcrframe.dy));
        param->ycbcrframe.clrPtr  = vse_malloc((param->ycbcrframe.dx)*(param->ycbcrframe.dy)/2);
        ipl_conv_frame(param->frame,&(param->ycbcrframe),param->transparent_value);
        ipl_convert_pixel_rgb565_ycrcb(param->transparent_value,&(param->y),&(param->cr),&(param->cb));
        param->new_frame = FALSE;
      }
      ipl_retval = ipl_add_overlay_inplace(input,&(param->ycbcrframe),
                                         NULL,param->y,param->cr,param->cb,0);

        if (ipl_retval != IPL_SUCCESS) {
          retval = VSE_FAILURE;
        }
      }
    packet->free_buffer_flag = INPUT_BUFFER_USED;

    MP4E_DEBUG_RECORD_TIMELOG(MP4E_TIMELOG_ARM_SPECIAL_EFFECT_DONE,NULL,0);
    MSG_VSETIMING("done overlay %d",mp4e_get_time_ms(),0,0);

    break;

  case VSE_INIT_ENG:

    if (vse_struct->engine_status == VSE_ENGINE_UNINITIALIZED) 
    {
      vse_struct->engine_status = VSE_ENGINE_INITIALIZED;
    }
    break;

  case VSE_INIT_PACKET:
    if ( packet->status != VSE_PACKET_STATUS_INITIALIZED ) {
      packet->status = VSE_PACKET_STATUS_INITIALIZED; 
    }
    else
    {
      retval = VSE_ALREADY_INITIALIZED;
    }
    break;

  case VSE_TERMINATE_PACKET:
    if ( packet->status == VSE_PACKET_STATUS_INITIALIZED) {
      packet->status = VSE_PACKET_STATUS_UNINITIALIZED;
    }
    else
    {
      retval = VSE_ALREADY_TERMINATED;
    }
    break;

  case VSE_TERMINATE:
    {
      vse_overlay_param_type *param;
      param = (vse_overlay_param_type *) packet->param;
      if (vse_struct->engine_status == VSE_ENGINE_INITIALIZED ) 
      {
        if(param->ycbcrframe.imgPtr)
        {
          vse_free(param->ycbcrframe.imgPtr);
        }
        if(param->ycbcrframe.clrPtr)
        {
          vse_free(param->ycbcrframe.clrPtr);
        }
        vse_struct->engine_status = VSE_ENGINE_UNINITIALIZED;
      }
      break;
    }
  }
  

  return retval;
}


typedef struct
{
  uint32 starting_msec;       /* msec of first frame */
  uint32 duration_msec;       /* fade in/fade out duration */
  vse_fadeInOutCommandType cmd_type;
} vse_fadeInOut_internal_type;


