static int disp_copy_yuv420(const byte *src, uint16 src_width, uint16 src_height)
{
    uint16 x,y;
    const byte *Cb_Index,*Cr_Index,*Y_Index;
    int16 Y1,Y2,Cb,Cr,R;
    uint16 out1,out2;
    
    Y_Index  = src;
    Cb_Index = Y_Index+src_width*src_height;
    Cr_Index = Cb_Index+((src_width*src_height)/4);
    
    for(y=0;y<src_height/2;y++)
    {
        // Line1
        for(x=0;x<src_width/2;x++)
        {
            /* Y_Channel */
            Y1=*(Y_Index++);
            Y2=*(Y_Index++);
            Y2-=Y1;
            
            /* U_Channel*/
            Cb=*(Cb_Index+x);
            //Cb-=128;
            
            /* V_Channel*/
            Cr=*(Cr_Index+x);
            //Cr-=128;

            R = Y1 + Cr2R[Cr];
            if ( R > 0 )
            {
                out1 = R5xx[R];
            }
            else
            {
                out1 = 0;
            }

            R += Y2;
            if ( R > 0 )
            {
                out2 = R5xx[R];
            }
            else
            {
                out2 = 0;
            }

            R = Y1 - Cb2G[Cb] - Cr2G[Cr];
            if ( R > 0 )
            {
                out1 += Gx6x[R];
            }

            R += Y2;
            if ( R > 0 )
            {
                out2 += Gx6x[R];
            }

            R = Y1 + Cb2B[Cb];
            if ( R > 0 )
            {
                out1 += Bxx5[R];
            }

            R += Y2;
            if ( R > 0 )
            {
                out2 += Bxx5[R];
            }
            LCD_WRITE_DATA16(out1);
            LCD_WRITE_DATA16(out2);
        }

        // Line2
        for(x=0;x<src_width/2;x++)
        {
            /* Y_Channel */
            Y1=*(Y_Index++);
            Y2=*(Y_Index++);
            Y2-=Y1;
            
            /* U_Channel*/
            Cb=*(Cb_Index++);
            //Cb-=128;
            
            /* V_Channel*/
            Cr=*(Cr_Index++);
            //Cr-=128;

            R = Y1 + Cr2R[Cr];
            if ( R > 0 )
            {
                out1 = R5xx[R];
            }
            else
            {
                out1 = 0;
            }

            R += Y2;
            if ( R > 0 )
            {
                out2 = R5xx[R];
            }
            else
            {
                out2 = 0;
            }

            R = Y1 - Cb2G[Cb] - Cr2G[Cr];
            if ( R > 0 )
            {
                out1 += Gx6x[R];
            }

            R += Y2;
            if ( R > 0 )
            {
                out2 += Gx6x[R];
            }

            R = Y1 + Cb2B[Cb];
            if ( R > 0 )
            {
                out1 += Bxx5[R];
            }

            R += Y2;
            if ( R > 0 )
            {
                out2 += Bxx5[R];
            }
            LCD_WRITE_DATA16(out1);
            LCD_WRITE_DATA16(out2);
        }
    }
    return 0;
}

static int disp_scale_yuv420(const byte *src, uint16 src_width, uint16 src_height, uint16 dst_width, uint16 dst_height)
{
    uint16 x,y,scale_x,scale_y,temp;
    uint32 scale;
    const byte *Cb_Index,*Cr_Index,*Y_Index, *Cb_Orig,*Cr_Orig,*Y_Orig;
    int16 Y1,Cb,Cr,R;
    uint16 out;
    int16 CbCrLineSize;
    
    CbCrLineSize = src_width/2;
    
    Y_Orig  = src;
    Cb_Orig = Y_Orig+src_width*src_height;
    Cr_Orig = Cb_Orig+((src_width*src_height)/4);
    scale    = (src_width<<10)/dst_width;
    
    for(y=0;y<dst_height;y++)
    {
        scale_y = (y*scale)>>10;
        Y_Index = Y_Orig+scale_y*src_width;
        temp = (scale_y>>1)*CbCrLineSize;
        Cb_Index = Cb_Orig+temp;
        Cr_Index = Cr_Orig+temp;
        
        // Line1
        for(x=0;x<dst_width;x++)
        {
            scale_x = (x*scale)>>10;
            
            /* Y_Channel */
            Y1=*(Y_Index+scale_x);
            
            /* U_Channel*/
            Cb=*(Cb_Index+(scale_x>>1));
            //Cb-=128;
            
            /* V_Channel*/
            Cr=*(Cr_Index+(scale_x>>1));
            //Cr-=128;
            
            R = Y1 + Cr2R[Cr];
            if ( R > 0 )
            {
                out = R5xx[R];
            }
            else
            {
                out = 0;
            }

            R = Y1 - Cb2G[Cb] - Cr2G[Cr];
            if ( R > 0 )
            {
                out += Gx6x[R];
            }
            
            R = Y1 + Cb2B[Cb];
            if ( R > 0 )
            {
                out += Bxx5[R];
            }

            LCD_WRITE_DATA16(out);
        }
    }
    return 0;
}

static void disp_ic_yuv420(const byte *src_ptr, word src_w, word src_h, word dst_w, word dst_h)
{
    if(src_w == dst_w && src_h == dst_h)
    {
        (void)disp_copy_yuv420(src_ptr, src_w, src_h);
    }
    else
    {
        (void)disp_scale_yuv420(src_ptr, src_w, src_h, dst_w, dst_h);
    }
}
