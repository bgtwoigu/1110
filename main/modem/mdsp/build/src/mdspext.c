/*===========================================================================

FUNCTION MDSP_FW_CTRL_FUNC

DESCRIPTION
  Control whether the MDSP is in Download or Run mode.

DEPENDENCIES
  The clock regime driving the MDSP/ARM shared memory
  must be enabled before using this function.

RETURN VALUE
  None

SIDE EFFECTS
  None
  
===========================================================================*/
void mdsp_fw_ctrl_func ( qdspext_fw_ctrl_type fw_state )
{
  if (fw_state == QDSPEXT_FW_DOWNLOAD)
  {
    /* Put the MDSP into Download Mode */
    #if (MDSP_REG_WORD_SIZE==1)
    (*(volatile uint16 *)MDSP_CTRL_REGISTER) = MDSP_RESET_VAL;
    #else
    (*(volatile uint32 *)MDSP_CTRL_REGISTER) = MDSP_RESET_VAL;
    #endif
  }
  else
  {
    /* Put the MDSP into Run Mode */
    #if (MDSP_REG_WORD_SIZE==1)
    (*(volatile uint16 *)MDSP_CTRL_REGISTER) = MDSP_RUN_VAL;
    #else
    (*(volatile uint32 *)MDSP_CTRL_REGISTER) = MDSP_RUN_VAL;
    #endif
  }
} /* mdsp_fw_ctrl_func */

