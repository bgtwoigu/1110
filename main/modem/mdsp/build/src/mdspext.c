/*===========================================================================

FUNCTION MDSP_DOWNLOAD_IMAGE

DESCRIPTION
  Parameters:
  - image, index to a download image descriptor

  Performs the download of the MDSP.

DEPENDENCIES
  The clock regime driving the MDSP/ARM shared memory
  must be enabled before using this function.

RETURN VALUE
  - MDSP_DOWNLOAD_SUCCESS - Firmware download successful.
  - MDSP_DOWNLOAD_FAIL    - Firmware download failed.

SIDE EFFECTS
  The MDSP will be downloaded, but the FW will not be running.

===========================================================================*/
mdsp_download_status_type mdsp_download_image (
  /* Index of the image to download */ 
  const mdsp_image_type image
)
{
  if (image < MDSP_IMAGE_MAX && mdsp_image_table[image] != NULL)
  {
    mdsp_pre_download_actions(image);
    mdsp_current_dma_table = (uint32*)mdsp_image_table[image]->dma_table;

    if ( qdspdown_download_image( (uint32) image, &mdsp_data_table ) ==
         QDSPDOWN_DOWNLOAD_COMPLETED )
    {
      mdsp_post_download_actions(image);
      return (MDSP_DOWNLOAD_SUCCESS);
    }
  }
  return (MDSP_DOWNLOAD_FAIL);
} /* mdsp_download_image */

