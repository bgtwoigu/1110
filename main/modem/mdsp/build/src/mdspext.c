/*===========================================================================

FUNCTION MDSP_BANK_ADDRESS

DESCRIPTION
  Returns an ARM address pointer given a bank-offset and MDSP memory bank.

DEPENDENCIES
  None

RETURN VALUE
  The ARM address of the MDSP variable.

SIDE EFFECTS
  None

===========================================================================*/
uint16* mdsp_bank_address (
  /* The memory bank to use as the base */
  mdsp_bank_type bank,
  /* The offset in words from the start of the memory bank */
  uint16 offset
)
{
  uint32 addr;

  if (bank == MDSP_BANK_RAMA) {
    addr = (offset << 1) + mdsp_data_table.rama_offset;
  } else if (bank == MDSP_BANK_RAMB) {
    addr = (offset << 1) + mdsp_data_table.ramb_offset;
  } else if (bank == MDSP_BANK_RAMC) {
    addr = (offset << 2) + mdsp_data_table.ramc_offset;
  } else if (bank == MDSP_BANK_RAMI) {
    addr = (offset << 2) + mdsp_data_table.rami_offset;
  } else {
    addr = 0;
  }
  return( (uint16*)(mdsp_data_table.base_address + addr) );
} /* mdsp_bank_address */

