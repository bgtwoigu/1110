/*===========================================================================

FUNCTION MDSP_VALID_ADDRESS

DESCRIPTION
  Checks if any address in a block of memory is within the
  MDSP/ARM shared memory area.

DEPENDENCIES
  None

RETURN VALUE
  TRUE  - Part of the memory block is within the shared memory area.
  FALSE - No part of the memory block is within the shared memory area.

SIDE EFFECTS
  None

===========================================================================*/
boolean mdsp_valid_address (
  /* The start address of the memory block */
  void* start_address,
  /* The length in bytes of the memory block */
  uint16 length
)
{
  byte* end_addr;

  /* Adjust length to be zero relative */
  if ( length != 0 )
  {
    length--;
  }

  /* Calculate end of block address */
  end_addr = (byte*)start_address + length;
  
  if ( ( (byte*)start_address < (byte*)mdsp_data_table.end_address ) &&
       ( end_addr >= (byte*)mdsp_data_table.base_address ) )
  {
    return TRUE;
  }
  else
  {
    return FALSE;
  }
} /* mdsp_valid_address */

