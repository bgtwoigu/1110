/*===========================================================================
FUNCTION ui_netwk_format_network_name

DESCRIPTION
  This function formats the network's name for display purposes.  The
  returned buffer (buffer_ptr) is not null terminated.  The number of
  characters in the buffer is returned to the calling routine.

  If the full name is provided and its length is <= UI_WIDE, then
  the the full name is copied to the buffer_ptr parameter.

  Otherwise, if the short name is provided, then the short name
  is copied to the buffer_ptr parameter.

  Otherwise, "<mcc>  <mnc>" is copied to the buffer_ptr parameter, where
  <mcc> is the country code and <mnc> is the network code.

RETURNS
  The number of characters in the buffer (buffer_ptr).

DEPENDENCIES
  The info parameter must have been initialized via a call to
  ui_netwk_get_network_info.  The length of the buffer_ptr
  parameter should be at least UI_WIDE+1.

===========================================================================*/
uint32 ui_netwk_format_network_name
(
  uinetwk_network_info_s_type   *info_ptr,
    /* The network info                                     */

  char                          *buffer_ptr
    /* The formatted network name to be displayed on the UE */
)
{
  uint32 length = 0;

  if ( buffer_ptr == NULL)
  {
     return length;
  }

  if ( info_ptr->full_name_ptr != NULL )
  {
     length = strlen( info_ptr->full_name_ptr );

     if ( length <= UI_WIDE )
     {
       (void) memcpy( buffer_ptr, info_ptr->full_name_ptr, length );
       return length;
     }
  }

  if ( info_ptr->short_name_ptr != NULL )
  {
    length = strlen( info_ptr->short_name_ptr );

    /*
    ** Make sure the text will fit on one line of the display.
    */
    length = MIN( length, UI_WIDE);

    (void) memcpy( buffer_ptr,
                   info_ptr->short_name_ptr,
                   length
                  );
  }
  else
  {
    char   buffer[UI_WIDE + 1];
    length = (uint32) snprintf( buffer, ARR_SIZE(buffer), "%u  %u",
                               (unsigned int) info_ptr->mcc,
                               (unsigned int) info_ptr->mnc );
    /*
    ** Strip off the null character
    */
    (void) memcpy( buffer_ptr, buffer, length );
  }

  return length;

} /* ui_netwk_format_network_name */

