/*===========================================================================
FUNCTION CONVERT_NUMBER_TO_ASCII

DESCRIPTION
  This function converts input number to ascii and returns the number of ascii
  that have been converted.

  ex) input_number = 365
      converted_ascii = "       365"
DEPENDENCIES
  None

SIDE EFFECTS
  None
===========================================================================*/
byte convert_number_to_ascii
(
  unsigned int input_number,        /* input */
  char*        converted_ascii,     /* where retured ascii is going to be*/
  byte         size_of_array        /* size of converted_ascii array */
)
{
  byte i=1;

  /* init */
  /* In case of input_number is 0, this is to be retunred */
  memset(converted_ascii, ' ', size_of_array);
  converted_ascii[size_of_array-1] = '0';

  if (input_number == 0)
    return i;

  while ((input_number != 0) && (size_of_array >= i))
  {
    converted_ascii[size_of_array-i] = '0' + (input_number%10);
    i++;
    input_number /= 10;
  }

  return (--i);
} /* convert_number_to_ascii */

