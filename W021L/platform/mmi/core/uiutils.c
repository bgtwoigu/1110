/*===========================================================================
FUNCTION ui_netwk_find_network_info_by_name

DESCRIPTION
  This function returns the Mobile Country Code (mcc), Mobile Network Code
  (mnc) and network name for the requested network, matching by name.
  The name_type is a bitmask indicating which name to search.
    NETWORK_NAMETYPE_SHORT is set to 1,
    NETWORK_NAMETYPE_FULL is set to 2,
    for both, both bits can be set.

RETURNS
  TRUE  - The PLMN Id was found in the lookup table.  The returned info
          parameter contains all relevant information regarding the network.
  FALSE - The PLMN Id was not found in the lookup table.  

DEPENDENCIES
  Function uinetwk_init must be called once before calling this function.

===========================================================================*/
boolean ui_netwk_find_network_info_by_name
(
  char                         *network_name,
    /* The PLMN id to be processed */

  uint32                         name_type,
    /* UI_NETWORK_NAMETYPE_SHORT for Short, UI_NETWORK_NAMETYPE_FULL for long, 
       UI_NETWORK_NAMETYPE_SHORT | UI_NETWORK_NAMETYPE_FULL for either. */

  uinetwk_network_info_s_type  *info_ptr
    /* The returned network info   */
)
{
  int i;                 /* Index into array. */
  boolean found = FALSE; /* Did we find a match? */

  if ((NULL == network_name) ||
      (((name_type & (UI_NETWORK_NAMETYPE_SHORT|UI_NETWORK_NAMETYPE_FULL)))==0))
    return FALSE;

  for (i = 0; !found && (i < ARR_SIZE(uinetwk_network_table)); i++)
  {
    /* Search for the SHORT name type if present. */
    if (name_type & UI_NETWORK_NAMETYPE_SHORT)
    {
      if (ui_strnicmp(network_name, uinetwk_network_table[i].short_name_ptr,
                              strlen(network_name)) ==0)
      {
        found = TRUE;
        break;
      }
    }

    /* Search for the FULL name type if present. */
    if (name_type & UI_NETWORK_NAMETYPE_FULL)
    {
      if (ui_strnicmp(network_name, uinetwk_network_table[i].full_name_ptr,
                              strlen(network_name)) == 0)
      {
        found = TRUE;
        break;
      }
    }
  }

  if (found)
  {
    info_ptr->mcc              = uinetwk_network_table[ i ].mcc;
    info_ptr->mnc              = uinetwk_network_table[ i ].mnc;
    info_ptr->network_type     = uinetwk_network_table[ i ].network_type;
    info_ptr->full_name_ptr    = uinetwk_network_table[ i ].full_name_ptr;
    info_ptr->short_name_ptr   = uinetwk_network_table[ i ].short_name_ptr;
  }

  return found;
} 

